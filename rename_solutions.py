"""
Rename all solution .cpp files to .hpp and update the Visual Studio project files.

This script:
1. Renames src/LeetCode/0*.cpp -> src/LeetCode/0*.hpp
2. Regenerates LeetCode.vcxproj with solutions as ClInclude (headers)
3. Regenerates LeetCode.vcxproj.filters with solutions under "Header Files"

Run from the repo root:
    python rename_solutions.py
"""

import os
import glob
from pathlib import Path

SOLUTION_DIR = Path("src/LeetCode")


def get_solution_files():
    """Get all solution files (starting with digits), sorted."""
    files = sorted(SOLUTION_DIR.glob("0*.cpp"))
    return [f.stem for f in files]


def rename_files(stems):
    """Rename .cpp to .hpp for all solution files."""
    renamed = []
    for stem in stems:
        src = SOLUTION_DIR / f"{stem}.cpp"
        dst = SOLUTION_DIR / f"{stem}.hpp"
        if src.exists():
            src.rename(dst)
            renamed.append(stem)
            print(f"  Renamed: {stem}.cpp -> {stem}.hpp")
    return renamed


