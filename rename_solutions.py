def generate_filters(hpp_stems):
    """Generate the .vcxproj.filters file."""
    
    cl_include_entries = "\n".join(
        f'    <ClInclude Include="{stem}.hpp">\n'
        f'      <Filter>Header Files</Filter>\n'
        f'    </ClInclude>'
        for stem in hpp_stems
    )

    content = f'''<?xml version="1.0" encoding="utf-8"?>
<Project ToolsVersion="4.0" xmlns="http://schemas.microsoft.com/developer/msbuild/2003">
  <ItemGroup>
    <Filter Include="Source Files">
      <UniqueIdentifier>{{4FC737F1-C7A5-4376-A066-2A32D752A2FF}}</UniqueIdentifier>
      <Extensions>cpp;c;cc;cxx;c++;cppm;ixx;def;odl;idl;hpj;bat;asm;asmx</Extensions>
    </Filter>
    <Filter Include="Header Files">
      <UniqueIdentifier>{{93995380-89BD-4b04-88EB-625FBE52EBFB}}</UniqueIdentifier>
      <Extensions>h;hh;hpp;hxx;h++;hm;inl;inc;ipp;xsd</Extensions>
    </Filter>
    <Filter Include="Resource Files">
      <UniqueIdentifier>{{67DA6AB6-F800-4c08-8B7A-83BB121AAD01}}</UniqueIdentifier>
      <Extensions>rc;ico;cur;bmp;dlg;rc2;rct;bin;rgs;gif;jpg;jpeg;jpe;resx;tiff;tif;png;wav;mfcribbon-ms</Extensions>
    </Filter>
  </ItemGroup>
  <ItemGroup>
    <ClCompile Include="LeetCode.cpp">
      <Filter>Source Files</Filter>
    </ClCompile>
  </ItemGroup>
  <ItemGroup>
{cl_include_entries}
  </ItemGroup>
</Project>
'''

    outpath = SOLUTION_DIR / "LeetCode.vcxproj.filters"
    outpath.write_text(content, encoding="utf-8")
    print(f"  Generated: {outpath}")


def main():
    print("=== LeetCode Solution Restructure ===\n")
    
    # Step 1: Discover solution files
    stems = get_solution_files()
    print(f"Found {len(stems)} solution files.\n")
    
    # Step 2: Rename .cpp -> .hpp
    print("Renaming files:")
    renamed = rename_files(stems)
    print(f"\n  Renamed {len(renamed)} files.\n")
    
    # Step 3: Regenerate .vcxproj
    print("Generating .vcxproj:")
    generate_vcxproj(renamed)
    
    # Step 4: Regenerate .vcxproj.filters
    print("\nGenerating .vcxproj.filters:")
    generate_filters(renamed)
    
    print("\n=== Done! ===")
    print("\nNext steps:")
